/*  Downloads the UI-chrome icons (Material Symbols Rounded, Apache 2.0) into
 *  src/assets/ui/ and writes a component that inlines them as SVG.
 *
 *  These are deliberately separate from the emoji in src/assets/icons/:
 *
 *    UI chrome  — gear, speaker, lock, close, back. Monochrome line/solid art
 *                 that inherits currentColor, so the same lock can be grey on a
 *                 locked node and the same speaker blue inside its button. An
 *                 emoji can't be recoloured, and a bright yellow padlock drew
 *                 the eye straight to the one thing the child can't tap.
 *
 *    Content    — lesson topics, stickers, word-problem objects. Those stay
 *                 colourful: a child scans the map and recognises "apple =
 *                 word problems", where 34 grey glyphs would all look alike.
 *
 *  Rounded, not the default cut, because everything in this app is rounded —
 *  the radii tokens, the buttons, Nunito itself.
 *
 *    node scripts/fetch-ui-icons.mjs
 */
import { mkdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const OUT = join(process.cwd(), 'src', 'assets', 'ui')
mkdirSync(OUT, { recursive: true })

const BASE = 'https://raw.githubusercontent.com/google/material-design-icons/master/symbols/web'

// name → [material folder, filled?]
const WANTED = {
  gear: ['settings', true],
  sound: ['volume_up', true],
  mute: ['volume_off', true],
  lock: ['lock', true],
  close: ['close', false],
  back: ['arrow_back', false],
  next: ['arrow_forward', false],
  check: ['check', false],
  trophy: ['trophy', true],
  replay: ['refresh', false],
  chart: ['bar_chart', true],
}

const out = []
const missing = []

for (const [key, [folder, filled]] of Object.entries(WANTED)) {
  const file = `${folder}_${filled ? 'fill1_' : ''}24px.svg`
  const url = `${BASE}/${folder}/materialsymbolsrounded/${file}`
  try {
    const res = await fetch(url)
    if (!res.ok) {
      missing.push(`${key} (HTTP ${res.status})`)
      continue
    }
    let svg = await res.text()
    // Keep only the path data — the component supplies the wrapper, so the
    // icons inherit size and currentColor from wherever they're used.
    const paths = [...svg.matchAll(/<path[^>]*\sd="([^"]+)"/g)].map((m) => m[1])
    if (!paths.length) {
      missing.push(`${key} (no path)`)
      continue
    }
    const viewBox = (svg.match(/viewBox="([^"]+)"/) || [, '0 -960 960 960'])[1]
    out.push({ key, viewBox, paths })
  } catch (e) {
    missing.push(`${key} (${e.message})`)
  }
}

const lines = [
  '/* GENERATED by scripts/fetch-ui-icons.mjs — do not edit by hand.',
  ' * Material Symbols Rounded, Apache 2.0 (google/material-design-icons).',
  ' * These are UI chrome and inherit currentColor; colourful content icons',
  ' * live in ./icons.js instead. */',
  '',
  'export const UI = {',
  ...out.map(
    (i) =>
      `  ${i.key}: { viewBox: ${JSON.stringify(i.viewBox)}, d: ${JSON.stringify(i.paths)} },`,
  ),
  '}',
  '',
]
writeFileSync(join(process.cwd(), 'src', 'assets', 'ui-icons.js'), lines.join('\n'))

const bytes = JSON.stringify(out).length
console.log(`✓ ${out.length} UI icons, ${(bytes / 1024).toFixed(1)} KB inline`)
if (missing.length) console.log('missing:', missing.join(', '))
